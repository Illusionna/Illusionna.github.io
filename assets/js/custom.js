function showSubmenu(id) {
    var submenu = document.getElementById(id + '-submenu');
    submenu.classList.toggle('show');
}
function showSubsubmenu(id) {
    var subsubmenu = document.getElementById(id + '-subsubmenu');
    subsubmenu.classList.toggle('show');
}
function showSubsubsubmenu(id) {
    var subsubsubmenu = document.getElementById(id + '-subsubsubmenu');
    subsubsubmenu.classList.toggle('show');
}
